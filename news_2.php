				<div class="main-news__title home-title">Как ухаживать за холодильником: 5 простых правил</div>
				<div class="main-news__body">
					Холодильник, как и&nbsp;любая бытовая техника, требует особого ухода. Если не&nbsp;следить за&nbsp;его
					состоянием, помощник по&nbsp;кухне будет хорошо справляться с&nbsp;работой несколько лет, а&nbsp;потом
					начнет жаловаться и&nbsp;просить вызвать мастера на&nbsp;дом. Отсюда&nbsp;&mdash; лишние траты
					на&nbsp;ремонт и&nbsp;сокращенный срок службы.<br>

					Если&nbsp;же держать прибор в&nbsp;благоприятных условиях и&nbsp;регулярно устраивать ему водные
					процедуры, он&nbsp;ответит вам взаимностью и&nbsp;будет работать долгие годы. Знакомьтесь с&nbsp;пятью
					простыми правилами ухода за&nbsp;холодильником, которые позволят сохранить бытовую технику
					в&nbsp;отличном состоянии.<br>

					<p>Оптимальное место для установки</p>

					Уход за&nbsp;холодильником начинается с&nbsp;выбора места установки. Часто владельцы электроприбора
					пропускают этот пункт и&nbsp;ставят холодильник рядом с&nbsp;плитой, допуская большую ошибку. Основное
					правило, которое нужно запомнить: не&nbsp;допускайте близости нагревательных приборов рядом
					с&nbsp;холодильником, и&nbsp;тем более они не&nbsp;должны его касаться. В&nbsp;список к&nbsp;плите
					можно добавить микроволновую печь и&nbsp;даже теплый пол, который постоянным нагревом заставляет
					компрессор работать безостановочно. Также, если окна вашей кухни выходят на&nbsp;солнечную сторону,
					лучше не&nbsp;допускать попадания прямых лучей на&nbsp;прибор. Этот момент можно регулировать шторами
					из&nbsp;плотной ткани.<br>

					Разумеется, все эти факторы не&nbsp;приведут к&nbsp;резкой поломке устройства, но&nbsp;могут
					значительно снизить срок его службы. Если есть возможность, лучше подыскать более безопасное место для
					вашего ледяного помощника.<br>


					<p>Разморозка</p>

					Если в&nbsp;холодильнике образовался слой наледи, это главная причина, чтобы разморозить прибор.
					Толстая &laquo;ледяная шуба&raquo; внутри морозилки не&nbsp;только уменьшает ее&nbsp;пространство,
					но&nbsp;и&nbsp;заставляет компрессор работать больше при меньшей эффективности, увеличивая при этом
					ваш счет за&nbsp;электричество. Холодильники старого формата стоит размораживать 1&nbsp;раз
					в&nbsp;несколько месяцев, электроприборы с&nbsp;системой&nbsp;No frost нуждаются в&nbsp;процедуре
					значительно реже. Итак, приступим.<br>

					Подготовьтесь к&nbsp;размораживанию холодильника заранее: накануне не&nbsp;покупайте новых продуктов,
					а&nbsp;старые постарайтесь заранее съесть, чтобы ничего не&nbsp;испортилось.
					Отрегулируйте температуру устройства до&nbsp;0&deg;C, затем отключите его от&nbsp;питания
					и&nbsp;откройте дверцы.<br>
					Если у&nbsp;вашего холодильника нет резервуара для сбора талой жидкости, положите под него несколько
					полотенец. Обычно эта мера требуется для старых моделей.
					Размораживайте холодильник в&nbsp;естественных условиях, на&nbsp;это может уйти
					от&nbsp;4&nbsp;до&nbsp;10&nbsp;часов, чтобы слой наледи полностью сошел. Опытные хозяйки рекомендуют
					оставлять прибор без электропитания на&nbsp;ночь.
					Не&nbsp;откалывайте, не&nbsp;скребите и&nbsp;не&nbsp;собирайте лед ножом. Так вы&nbsp;можете повредить
					не&nbsp;только пластик, но&nbsp;и&nbsp;канал под ним, который является важной частью механизма. Если
					вы&nbsp;решили воспользоваться лайфхаком с&nbsp;феном, позволяющим намного быстрее растопить наледь,
					не&nbsp;направляйте поток горячего воздуха на&nbsp;уплотнительную резинку&nbsp;&mdash; она может
					высушиться и&nbsp;деформироваться. В&nbsp;таком случае холодильник станет неплотно закрываться,
					и&nbsp;наледь начнет нарастать быстрее.<br>
					С&nbsp;помощью тряпки удалите отколотый лед и&nbsp;талую воду. Промойте полки и&nbsp;контейнеры.
					Обязательно высушите стенки холодильника, можно просто протереть их&nbsp;сухой тряпкой.
					Затем включите холодильник. Подождите полчаса, пока температура не&nbsp;понизится до&nbsp;оптимальной,
					и&nbsp;наполняйте полки продуктами.

					<p>Средства для мытья холодильника</p>

					Основательно мыть холодильник нужно как раз во&nbsp;время разморозки, когда отошла наледь. Покупные
					средства для мытья окон или домашних поверхностей не&nbsp;подойдут. Мы&nbsp;поделимся с&nbsp;вами
					рецептами подходящих растворов для мытья холодильника.<br>

					Содовый раствор. Приготовьте средство из&nbsp;расчета 2&nbsp;ст.&nbsp;л. соды на&nbsp;1&nbsp;л воды.
					Для мытья одного холодильника понадобится где-то 3&nbsp;л воды. После того как вымоете поверхности
					раствором, удалите остатки соды чистой тряпкой.<br>

					Средство на&nbsp;основе уксуса. Приготовьте раствор из&nbsp;расчета 2&nbsp;ст.&nbsp;л. уксуса
					на&nbsp;1&nbsp;л воды. Протрите холодильник и&nbsp;затем пройдитесь по&nbsp;поверхностям влажной
					тряпкой.<br>

					Бюджетное домашнее средство не&nbsp;только хорошо отмывает въевшиеся пятна, но&nbsp;и&nbsp;поглощает
					запахи, не&nbsp;оставляя при этом постороннего аромата. Для удобства раствор можно налить
					в&nbsp;пульверизатор.<br>

					<p>Инструкция по&nbsp;уборке</p>

					Во&nbsp;время разморозки холодильника вымыть его нужно изнутри. А&nbsp;затем не&nbsp;забудьте
					позаботиться и&nbsp;о&nbsp;наружной стороне прибора. Делимся пошаговой инструкцией мытья
					холодильника.<br>

					Снимите все полки и&nbsp;положите их&nbsp;в&nbsp;ванну, так удобнее будет мыть. Воспользуйтесь
					раствором домашнего приготовления. Если поверхности не&nbsp;сильно грязные, можно использовать обычную
					воду. Чистые полки вытрите насухо.<br>
					Смочите тряпку средством на&nbsp;основе соды или уксуса и&nbsp;тщательно протрите стенки холодильника.
					Вытрите насухо.
					Теперь достаньте контейнеры для мяса и&nbsp;овощей, замочите их&nbsp;на&nbsp;несколько минут. Ототрите
					пятна и&nbsp;хорошо прополоскайте емкости. Вытрите насухо.
					С&nbsp;помощью старой зубной щетки вымойте углы холодильника и&nbsp;резиновую прослойку.
					Затем протрите полки на&nbsp;дверце и&nbsp;установите все обратно в&nbsp;камеру.
					Вымойте дверь холодильника и&nbsp;бока. Уделите внимание петлям, здесь вы&nbsp;можете использовать
					щетку.
					Отодвиньте холодильник от&nbsp;стены. Используйте пылесос, чтобы собрать пыль, пух, шерсть с&nbsp;пола
					и&nbsp;вокруг компрессора холодильника&nbsp;&mdash; такая очистка поможет улучшить эффективность
					работы.
					Дезинфицируйте поддон, расположенный в&nbsp;нижней части холодильника, так как он&nbsp;быстро
					становится питательной средой для бактерий.<br>
					Теперь можно протереть холодильник сверху. Кстати, не&nbsp;рекомендуем хранить на&nbsp;нем тяжелые
					предметы.<br>

					<p>Избавление от&nbsp;запахов</p>

					Если разморозка и&nbsp;мытье холодильника не&nbsp;помогли удалить неприятный запах, есть несколько
					способов исправить ситуацию. Поместите один из&nbsp;продуктов, поглощающих запах, в&nbsp;холодильник,
					и&nbsp;нежеланные ароматы в&nbsp;скором времени покинут камеру.<br>

					Сода. Насыпьте ее&nbsp;в&nbsp;глубокую емкость или просто поставьте открытую пачку
					в&nbsp;холодильник.<br>

					Кофе. Это могут быть зерна или гуща, которая осталась от&nbsp;приготовления напитка.<br>

					Соль и&nbsp;сахар. Эти продукты можно держать в&nbsp;холодильнике круглый год, меняя каждые пару
					недель. Высыпьте на&nbsp;тарелку несколько ложек сахара или соли. Также можно их&nbsp;совместить.<br>

					Активированный уголь. Измельчите 10&ndash;20 таблеток активированного угля и&nbsp;высыпьте
					на&nbsp;тарелку. Держите в&nbsp;холодильнике не&nbsp;более 2&nbsp;суток.<br>

					Цитрусовые. Апельсин или лимон нарежьте кружочками и&nbsp;поставьте в&nbsp;холодильник
					на&nbsp;1&nbsp;день. Также для профилактики вы&nbsp;можете использовать корочки от&nbsp;плодов,
					но&nbsp;с&nbsp;сильным запахом они не&nbsp;справятся.<br>

					Еще сейчас в&nbsp;магазинах продаются искусственные поглотители запахов. Поместите баночку или саше
					в&nbsp;камеру и&nbsp;оставьте на&nbsp;тот срок, что указан на&nbsp;упаковке. И&nbsp;пусть холодильник
					прослужит вам долгие годы!

